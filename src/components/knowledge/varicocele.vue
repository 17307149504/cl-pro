精索静脉曲张可以引起睾丸的坠胀感、隐痛、不适等症状，久站、步行后症状可加重，平卧后可缓解或消失。
目前，将患者的情况分为四级。一种就是触诊的时候摸不到曲张静脉，通过超声检查才可以发现轻微的精索静脉曲张，这种是亚临床型。I级是只有在屏气增加腹压之后，才能摸到精索静脉曲张。II级是指休息时，可以摸到静脉曲张，但肉眼看不到阴囊表面蚯蚓状突起。III级是指休息时，既能摸到，也能看到曲张的静脉。
